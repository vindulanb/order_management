    private void btndeletememActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeletememActionPerformed

        MemberDetails md = new MemberDetails();

        int x = JOptionPane.showConfirmDialog(this,"Do You Really Want To Delete?");

        if(x == 0)
        {
            md.setMemID(Integer.parseInt(lblgetid.getText()));
            boolean result = dbOps.deleteMember(md);

            if(result)
            {
                JOptionPane.showMessageDialog(this, "Successfully Deleted the Member !!");
                userTableLoad();
                lblgetid.setText("");

            }
            else
            {
                JOptionPane.showMessageDialog(this, "An error occured while Deleting !!");
                userTableLoad();
                clearFields();

            }
        }

    }//GEN-LAST:event_btndeletememActionPerformed

