    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed

        ProductDetails pro = new ProductDetails();

        int x = JOptionPane.showConfirmDialog(this,"Do You Really Want To Delete?");

        if(x == 0)
        {
            pro.setRegID(Integer.parseInt(lblID.getText()));
            pro.setName(txtName.getText());
            pro.setPrice(Integer.parseInt(txtPrice.getText()));
            pro.setSeller(txtSeller.getText());
            pro.setPieces(Integer.parseInt(txtPiece.getText()));

            boolean result = dbOps.deleteProduct(pro);

            if(result)
            {
                JOptionPane.showMessageDialog(this, "Successfully Deleted the Product !!");
                tableLoad();
                clearFields();

            }
            else
            {
                JOptionPane.showMessageDialog(this, "An error occured while Deleting !!");
                tableLoad();
                clearFields();

            }
        }

    }//GEN-LAST:event_btnRemoveActionPerformed

